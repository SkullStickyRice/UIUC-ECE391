/* 
 * ASCII_graph_bar
 *   DESCRIPTION: 
                This function will writes the input texts to graphics image.
                To do so, this function will find the corresponding bit in the input texts, and save it in to the buffer, and print them on the screen.
                This function will also check if the input texts is status message or not, if not, it will save the room name,
                or the current typing inputs in the buffer.
 *   INPUTS: buffer-array containing the data from the font_data
             status_msg: status message
 *           input_typing: user input string
 *           room_name: name of the current room
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS:
 */
#define BUFFER_SIZE 5760        //320*18
#define BUFFER_W 80             //
#define FONT_H 16               // char's hight
#define FONT_W 8                // char's wide
#define FONT_COLOR 30 
#define BACKGROUND_COLOR 10

#define STATUS_BAR_MEM_SIZE 1440 // 320*18/4
#define MEM_FONT_W 2            // char's wide bitwise = 8/4
#define MAX_CHAR 40 // maximum number of character = 40
#define PLANE 4 // plane camprision



void ASCII_graph_bar( unsigned char *buffer, const char *status_msg, const char *current_room_name, const char* current_typing)
{
    int i;
    //initialization: put the background color in the buffer
    for (i = 0; i < BUFFER_SIZE; i++) { 
        buffer[i] = BACKGROUND_COLOR;
    }

    int char_index;
    int show_x;
    int show_y;
    unsigned char bit_mask;
    //int plane = 4 ;
    int start_point;
    
    int status_char;
    int status_len = strlen(status_msg);
    int room_char;
    int room_len = strlen(current_room_name);
    int type_char;
    int type_len = strlen(current_typing);
    //check weither there is status message or not
    if (status_msg[0] != '\00') {                // if there is a status message, print it. 

        for (char_index = 0; char_index < status_len; char_index++) {      // loop over each character in the string
            
            status_char = status_msg[char_index];                       
            
            for (show_y = 0; show_y < FONT_H; show_y++) {       // loop over char height
                
                bit_mask = 0x80;                //
                
                for (show_x = 0; show_x < FONT_W; show_x++) {    // loop over char width
                
                    if ( (bit_mask & font_data[status_char][show_y]) == bit_mask) { // bitwise comparison, check if it needs to draw
                        if (show_x < PLANE) { //if show_x is one of the first 4 pixel, save it to buffer 
                            start_point = ((MAX_CHAR - status_len)/2 + char_index) * MEM_FONT_W; // in the middle of the status bar
                            buffer[BUFFER_W + show_y*BUFFER_W + (show_x%PLANE)*STATUS_BAR_MEM_SIZE + start_point] = FONT_COLOR; // find it in the buffer
                        }
                        else { // if show_x is one of the second half of the char, save it to offset in the buffer +1
                            start_point = ((MAX_CHAR - status_len)/2 + char_index) * MEM_FONT_W; // 
                            buffer[BUFFER_W + show_y*BUFFER_W + ((show_x%PLANE)*STATUS_BAR_MEM_SIZE + 1) + start_point] = FONT_COLOR; // find it in the buffer with +1
                        }
                    }
                    bit_mask >>= 1; // right shift 
                }

            }
        }
    }
    else { // there is no status_msg, print out room name, and current typing

        for (char_index = 0; char_index < room_len; char_index++) { // print room name, save procedure as above

            room_char = current_room_name[char_index];

            for (show_y = 0; show_y < FONT_H; show_y++) {

                bit_mask = 0x80;

                for (show_x = 0; show_x < FONT_W; show_x++) {

                    if( (bit_mask & font_data[room_char][show_y]) == bit_mask) {
                        if (show_x < PLANE) {
                            start_point = char_index * MEM_FONT_W; // on the right side of the status bar
                            buffer[BUFFER_W + show_y*BUFFER_W + (show_x%PLANE)*STATUS_BAR_MEM_SIZE + start_point] = FONT_COLOR;
                        }
                        else {
                            start_point = char_index * MEM_FONT_W;
                            buffer[BUFFER_W + show_y*BUFFER_W + ((show_x%PLANE)*STATUS_BAR_MEM_SIZE + 1) + start_point] = FONT_COLOR;
                        }
                    }
                    bit_mask >>= 1;
                }
            }
        }

        for (char_index = 0; char_index < type_len; char_index++) {

            type_char = current_typing[char_index];

            for (show_y = 0; show_y < FONT_H; show_y++) {

                bit_mask = 0x80;

                for (show_x = 0; show_x < FONT_W; show_x++) {

                    if( (bit_mask & font_data[type_char][show_y]) == bit_mask) {
                        if (show_x < PLANE) {
                            start_point = (MAX_CHAR - type_len + char_index) * MEM_FONT_W; // at the lift side of the status bar
                            buffer[BUFFER_W + show_y*BUFFER_W + (show_x%PLANE)*STATUS_BAR_MEM_SIZE + start_point] = FONT_COLOR;
                        }
                        else {
                            start_point = (MAX_CHAR - type_len + char_index) * MEM_FONT_W;
                            buffer[BUFFER_W + show_y*BUFFER_W + ((show_x%PLANE)*STATUS_BAR_MEM_SIZE + 1) + start_point] = FONT_COLOR;
                        }
                    }
                    bit_mask >>= 1;
                }
            }
        }

    }
   
}
